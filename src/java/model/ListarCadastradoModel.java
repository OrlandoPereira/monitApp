
package model;


public class ListarCadastradoModel {
    
    private String nomeServico;
    private String prioridade;
    private String server;
    private String check;
    private String info;
    private String gpEmail;
    private String gpWhats;

    public ListarCadastradoModel() {
    }

    public String getNomeServico() {
        return nomeServico;
    }

    public void setNomeServico(String nomeServico) {
        this.nomeServico = nomeServico;
    }

    public String getPrioridade() {
        return prioridade;
    }

    public void setPrioridade(String prioridade) {
        this.prioridade = prioridade;
    }

    public String getServer() {
        return server;
    }

    public void setServer(String server) {
        this.server = server;
    }

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getGpEmail() {
        return gpEmail;
    }

    public void setGpEmail(String gpEmail) {
        this.gpEmail = gpEmail;
    }

    public String getGpWhats() {
        return gpWhats;
    }

    public void setGpWhats(String gpWhats) {
        this.gpWhats = gpWhats;
    }
   
    
}
